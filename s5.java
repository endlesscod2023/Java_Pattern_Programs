//Program to print five stars five times in a different row (square).
class s5
{
	public static void main(String[] args) 
	{
		for(int j=1;j<=5; j++)
			{
			for(int i=1; i<=5; i++ )
				{
				System.out.print("*");
				}
				System.out.println();
			}
	}
}
